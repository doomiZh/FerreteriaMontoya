    String userLogado;
    
    /**
     * Método que comprueba los campos de texto si estan vacios o no, en caso cuyo este vacio un
     * campo se mandara una alerta al usuario para que rellene los campos y 
     * colorea el campo vacio de color rojo
     * @return true en caso campos esten completos 
     * y false en caso que haya un campo sin rellenar
     */
    public boolean CompruebaCampos(){
        for (Component c : panelCampos.getComponents()){
            if (c instanceof JTextField campo){
                if (campo.getText().isBlank()){
                    JOptionPane.showMessageDialog(this, "El campo "+ campo.getName() + " es obligatorio.");
                    campo.setBackground(Color.red);
                    return false;
                }
            }
        }
        return true;
    }
    
    /**
     * Método para imprimir los datos de usuario en los campos de nombre completo,
     * nombre de usuario y contraseña
     */
    public void MostrarDatosUsuario(){
        userLogado = Login.usuario;
        Usuarios u = Conexion.MostrarFormularioUsuarios(userLogado);
        txtNombre.setText(u.getNombresCompletos());
        txtUsuario.setText(u.getUsuario());
        pfContrasenya.setText(u.getPass());
    }// End Method MostrarDatosUsuario();
    
    /**
     * Método para actualizar las cuentas donde actualizara el nombre y apellidos, y contraseña
     * enviando el usuario como parámetro principal en el metodo ActualizarCuenta.
     */
    public void Actualizar(){
        if(CompruebaCampos()){
            Usuarios u = new Usuarios(
            txtNombre.getText(),
            txtUsuario.getText(),
            new String(pfContrasenya.getPassword()).trim()
            );
            if(Conexion.ActualizarCuenta(u)){
                JOptionPane.showMessageDialog(this, "Usuario actualizado correctamente");
            } else {
                JOptionPane.showMessageDialog(this, "Error al actualizar tu cuenta.\nIntentalo mas tarde.");
            }
            MostrarDatosUsuario();
        }
    }// End Method Actualizar();

